print('nos dê dois números e mostraremos diversos calculos que podem ser feitos com eles:')
n1 = float(input ('escolha um número'))
n2 = float(input ('escolha outro número'))

print ('{} + {} é: {:.1f}!'.format (n1, n2, n1+n2))
print ('{} - {} é: {:.1f}!'.format (n1, n2, n1-n2))
print ('{} / {} é: {:.1f}!'.format (n1, n2 ,n1/n2))
print ('{} * {} é: {:.1f}!'.format (n1, n2, n1*n2))




print('nos dê dois números e mostraremos diversos calculos que podem ser feitos com eles:')
n1 = int(input('escolha um número'))
n2 = int(input('escolha outro número'))

print ('{} + {} é: {}!'.format (n1, n2, n1+n2))
print ('{} - {} é: {}!'.format (n1, n2, n1-n2))
print ('{} / {} é: {}!'.format (n1, n2 ,n1/n2))
print ('{} * {} é: {}!'.format (n1, n2, n1*n2))



